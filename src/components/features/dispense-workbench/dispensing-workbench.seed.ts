import type {
  DispenseMethod,
  SeedDrugRow,
  SeedPatient,
  SeedSectionRow,
} from './dispensing-workbench.types';

/** セクション見出し行ファクトリ（method 既定 '一包化'）*/
function sec(label: string, method?: DispenseMethod): SeedSectionRow {
  return { t: 'sec', label, method: method ?? '一包化' };
}

/** d() に渡す追加プロパティ（funsai/note/tag/chg/prevText）*/
type DrugOverrides = Partial<
  Pick<SeedDrugRow, 'tag' | 'funsai' | 'note' | 'chg' | 'prevText'>
>;

/**
 * 薬剤行ファクトリ。設計プロトの
 * `d=(name,yoho,a,h,y,n,o)=>Object.assign({...defaults},o||{})` を忠実再現。
 * 既定: a/h/y/n は '' / tag:'' / funsai:false / note:''。
 */
function d(
  name: string,
  yoho: string,
  a?: string,
  h?: string,
  y?: string,
  n?: string,
  o?: DrugOverrides,
): SeedDrugRow {
  return Object.assign(
    {
      t: 'drug' as const,
      name,
      yoho,
      a: a ?? '',
      h: h ?? '',
      y: y ?? '',
      n: n ?? '',
      tag: '',
      funsai: false,
      note: '',
    },
    o ?? {},
  );
}

